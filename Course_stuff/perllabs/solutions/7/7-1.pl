#	Explain the meaning of each of the following regular expressions:

	/the/			'the' anywhere
 
	/^the/			Begins with 'the'

	/the$/			Ends with 'the'

	/^the$/			Exactly equal to 'the' 

	/^the.*the$/		Begins and ends with 'the'

	/^the.*the$|^the$/	Begins and ends with 'the' OR 'the' by itself

	/h.t/			The letter 'h' followed by any one character followed by
				the letter 't'

	/h.*t/			The letter 'h' followed by zero or more characters followed by
				the letter 't'	


